package com.xian52.domain;

public class Pic {
	String id_;
	String parent_id;
	String url_;
	String path_;
	String alt_;
	String width_;
	String height_;
	
	public String getId_() {
		return id_;
	}
	public void setId_(String id_) {
		this.id_ = id_;
	}
	public String getParent_id() {
		return parent_id;
	}
	public void setParent_id(String parent_id) {
		this.parent_id = parent_id;
	}
	public String getUrl_() {
		return url_;
	}
	public void setUrl_(String url_) {
		this.url_ = url_;
	}
	public String getPath_() {
		return path_;
	}
	public void setPath_(String path_) {
		this.path_ = path_;
	}
	public String getAlt_() {
		return alt_;
	}
	public void setAlt_(String alt_) {
		this.alt_ = alt_;
	}
	public String getWidth_() {
		return width_;
	}
	public void setWidth_(String width_) {
		this.width_ = width_;
	}
	public String getHeight_() {
		return height_;
	}
	public void setHeight_(String height_) {
		this.height_ = height_;
	}
	
}
